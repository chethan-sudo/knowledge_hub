update_doc("Rate Limiting Layers", """# Rate Limiting Layers

Rate limiting on Emergent operates at multiple layers, each protecting different resources. Understanding these layers helps you debug "why is my request failing?" scenarios.

## The Rate Limiting Stack

```mermaid
flowchart TD
    REQ[Incoming Request] --> L1[Layer 1: Kubernetes Ingress<br/>Connection-level limits]
    L1 --> L2[Layer 2: Nginx<br/>Request rate limits]
    L2 --> L3[Layer 3: FastAPI Middleware<br/>Application-level limits]
    L3 --> L4[Layer 4: LLM Proxy<br/>Token budget limits]
    L4 --> APP[Application Logic]
```

## Layer Details

### Layer 1: Kubernetes Ingress

| Setting | Value | Purpose |
|---------|-------|---------|
| Max connections per IP | 100 | Prevent connection flooding |
| Request body size | ~5MB | Prevent memory exhaustion |
| Connection timeout | 60s | Free up stale connections |
| WebSocket timeout | 3600s | Allow long-lived connections |

**When you hit this:** You'll see a `413 Request Entity Too Large` or `503 Service Unavailable` error. This is the outermost layer — your request never reaches your application.

### Layer 2: Nginx Reverse Proxy

| Setting | Value | Purpose |
|---------|-------|---------|
| Requests per second | Varies | Prevent request flooding |
| Burst allowance | Yes | Short spikes are allowed |
| Proxy pass timeout | 120s | Prevent hung backends |

**When you hit this:** You'll see a `429 Too Many Requests` response from Nginx. The response won't have your application's error format — it'll be a plain Nginx error page.

### Layer 3: Application Middleware

This is where **you** control rate limiting in your FastAPI app:

```python
from fastapi import Request
from datetime import datetime, timezone
import time

# Simple in-memory rate limiter
rate_limit_store = {}

async def rate_limit_middleware(request: Request, call_next):
    client_ip = request.client.host
    now = time.time()
    
    # Clean old entries
    rate_limit_store[client_ip] = [
        t for t in rate_limit_store.get(client_ip, [])
        if now - t < 60  # 1-minute window
    ]
    
    # Check limit
    if len(rate_limit_store.get(client_ip, [])) >= 100:
        return JSONResponse(
            status_code=429,
            content={"detail": "Too many requests. Try again in a minute."}
        )
    
    # Record request
    rate_limit_store.setdefault(client_ip, []).append(now)
    return await call_next(request)
```

### Layer 4: LLM Proxy / Token Budget

The most nuanced layer — controls AI usage based on your balance:

| Check | What Happens | Error |
|-------|-------------|-------|
| Balance check before LLM call | If balance < estimated cost, call rejected | "Insufficient balance" |
| Token counting after call | Actual tokens deducted from balance | Balance updated |
| Rate limit per model | Provider-specific limits | "Rate limited by provider" |

## How to Identify Which Layer Blocked You

| Error Response | Layer | What to Do |
|---------------|-------|-----------|
| `413 Request Entity Too Large` | Kubernetes Ingress | Reduce payload size or use chunked uploads |
| `429` with Nginx HTML page | Nginx | Wait and retry, reduce request frequency |
| `429` with JSON `{"detail": "..."}` | Your Application | Check your rate limit settings |
| `402` or `{"error": "Insufficient balance"}` | LLM Proxy | Add balance in Profile → Universal Key |
| `503 Service Unavailable` | Any layer | Pod may be starting up — wait and retry |

## Implementing Rate Limiting in Your App

### Per-User Rate Limiting

```python
from collections import defaultdict
import asyncio

class RateLimiter:
    def __init__(self, max_requests=60, window_seconds=60):
        self.max_requests = max_requests
        self.window = window_seconds
        self.requests = defaultdict(list)
    
    def is_allowed(self, user_id: str) -> bool:
        now = time.time()
        # Remove expired entries
        self.requests[user_id] = [
            t for t in self.requests[user_id]
            if now - t < self.window
        ]
        if len(self.requests[user_id]) >= self.max_requests:
            return False
        self.requests[user_id].append(now)
        return True

limiter = RateLimiter(max_requests=30, window_seconds=60)
```

### Rate Limiting Best Practices

| Practice | Why |
|----------|-----|
| Use sliding windows, not fixed windows | Prevents burst at window boundaries |
| Return `Retry-After` header | Tells clients when to retry |
| Different limits for different endpoints | AI endpoints need stricter limits than static data |
| Log rate limit hits | Monitor for abuse patterns |
| Exempt health check endpoints | Monitoring shouldn't be rate-limited |

## Troubleshooting Rate Limits

| Symptom | Diagnosis | Fix |
|---------|-----------|-----|
| Random 429 errors | Nginx-level rate limiting | Check if you're making rapid sequential calls |
| 413 on file upload | Body size limit | Use chunked upload |
| AI features stop working | Token budget exhausted | Check balance in Profile → Universal Key |
| All requests failing | Pod starting up (503) | Wait 30 seconds, retry |
| Only POST requests failing | CSRF or rate limit on mutations | Check middleware configuration |
""")


update_doc("SSL/TLS & CORS", """# SSL/TLS & CORS

Security at the transport and browser level. Understanding these is essential for debugging "why can't my frontend talk to my backend?" problems.

## TLS on Emergent

```mermaid
flowchart LR
    B[Browser] -->|HTTPS| I[Ingress<br/>TLS Termination]
    I -->|HTTP| N[Nginx]
    N -->|HTTP| A[Your App<br/>Port 8001/3000]
```

**TLS terminates at the Kubernetes Ingress.** Your application code only sees plain HTTP traffic. This means:

| Layer | Protocol | Who Handles TLS? |
|-------|----------|-------------------|
| Browser ↔ Ingress | HTTPS (TLS 1.3) | Kubernetes cert-manager (automatic) |
| Ingress ↔ Nginx | HTTP | No TLS needed (internal) |
| Nginx ↔ App | HTTP | No TLS needed (internal) |

**You never need to configure SSL certificates in your app.** Emergent handles this automatically through Let's Encrypt certificates managed by cert-manager.

## CORS: Cross-Origin Resource Sharing

CORS is the browser's security mechanism that blocks frontend JavaScript from making requests to a different origin.

### What Is an "Origin"?

```
https://my-app.preview.emergentagent.com:443
  │          │                              │
  scheme     host                          port
```

Two URLs have the **same origin** only if scheme + host + port all match. Any difference = cross-origin.

### How CORS Works

```mermaid
sequenceDiagram
    participant B as Browser
    participant S as Server
    B->>S: OPTIONS /api/data (preflight)
    Note right of B: "Can I make a POST from this origin?"
    S->>B: 200 OK + CORS headers
    Note left of S: "Yes, these origins/methods are allowed"
    B->>S: POST /api/data (actual request)
    S->>B: 200 OK + data + CORS headers
```

**Preflight requests** happen automatically for "non-simple" requests (POST with JSON, custom headers, etc.). The browser sends an OPTIONS request first to check permissions.

### CORS Configuration on Emergent

```python
from starlette.middleware.cors import CORSMiddleware

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],  # In production, list specific origins
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)
```

**On Emergent, CORS is configured with `allow_origins=["*"]`** because the preview URL is dynamic and changes between sessions. In a production deployment, you'd restrict this to your specific domain.

### CORS Headers Explained

| Header | Purpose | Example |
|--------|---------|---------|
| `Access-Control-Allow-Origin` | Which origins can access | `*` or `https://example.com` |
| `Access-Control-Allow-Methods` | Which HTTP methods are allowed | `GET, POST, PUT, DELETE` |
| `Access-Control-Allow-Headers` | Which request headers are allowed | `Content-Type, Authorization` |
| `Access-Control-Allow-Credentials` | Allow cookies/auth headers | `true` or `false` |
| `Access-Control-Max-Age` | How long to cache preflight response | `3600` (1 hour) |

## Common Security Headers

Beyond CORS, production apps should include:

| Header | Purpose | Value |
|--------|---------|-------|
| `X-Content-Type-Options` | Prevent MIME sniffing | `nosniff` |
| `X-Frame-Options` | Prevent clickjacking | `DENY` or `SAMEORIGIN` |
| `Strict-Transport-Security` | Force HTTPS | `max-age=31536000; includeSubDomains` |
| `Content-Security-Policy` | Control resource loading | Complex; domain-specific |

## Troubleshooting CORS Issues

CORS errors are the #1 frustration for frontend developers. Here's how to debug them:

### The Error Message

```
Access to fetch at 'https://api.example.com/data' from origin 
'https://my-app.com' has been blocked by CORS policy: 
No 'Access-Control-Allow-Origin' header is present.
```

### Decision Tree

| Check | If Yes | If No |
|-------|--------|-------|
| Is the backend running? | Continue | Start the backend first |
| Can you curl the endpoint? | CORS is the issue | Backend bug, not CORS |
| Does the response have CORS headers? | Headers are wrong | CORS middleware not applied |
| Is it a preflight (OPTIONS) request? | OPTIONS handler missing | Check allowed origins |
| Does the origin match allowed origins? | Check methods/headers | Add origin to allowed list |

### Common CORS Fixes

```python
# Fix 1: Ensure CORS middleware is added BEFORE routes
app = FastAPI()
app.add_middleware(CORSMiddleware, ...)  # Must be before router
app.include_router(api_router)           # After middleware

# Fix 2: Don't set CORS headers manually AND use middleware
# Pick one approach, not both — they conflict

# Fix 3: For cookies/auth, credentials must be true
# AND allow_origins cannot be "*" (use specific origins)
```

### Why CORS Only Affects Browsers

| Client | Subject to CORS? | Why |
|--------|-----------------|-----|
| Browser (fetch/XHR) | Yes | Browser enforces same-origin policy |
| curl | No | Command-line tools don't have origins |
| Postman | No | Not a browser, no same-origin policy |
| Server-to-server | No | CORS is a browser-only mechanism |
| Mobile app (native) | No | Native apps don't have origins |

This is why "it works in Postman but not in my app" is almost always a CORS issue.

## HTTPS Best Practices

| Practice | Why |
|----------|-----|
| Never mix HTTP and HTTPS | Mixed content is blocked by browsers |
| Use relative URLs for API calls | Automatically uses the page's protocol |
| Set `Secure` flag on cookies | Prevents cookies from being sent over HTTP |
| Use `REACT_APP_BACKEND_URL` | Ensures correct protocol in all environments |
""")

